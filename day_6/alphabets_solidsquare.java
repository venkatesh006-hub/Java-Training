package day_6;

public class alphabets_solidsquare
{
    public static void main(String[] args)
    {
        char cn ='A';
        for(int i=1;i<=5;i++)
        {
            for(int j=1;j<=5;j++)
            {
                System.out.print(cn);
                cn++;

            }
            System.out.println();
        }
    }
}
